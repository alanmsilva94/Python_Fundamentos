import math

# Arredondamento de número inteiro para cima
print(math.ceil(4.2))
# Arredondamento de número inteiro para baixo
print(math.floor(4.2))
# Converte em número absoluto
print(math.fabs(-1))
# Informa o resto da divisão
print(math.fmod(9,4))
# Raiz quadrada
print(math.sqrt(36))
# Arredondamento (Não é da Math, mas é bastante utilizada)
print(round(5.753,12))
# Raiz cubica
print(math.cbrt(3))
# Elevaçao de número x^y
print(math.pow(2,3))

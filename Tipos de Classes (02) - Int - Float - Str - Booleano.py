#Tipos de atribuições

Idade = 29
Nascimento = 1994
print(type(Idade))
print(type(Nascimento))

altura = 1.70
peso = 62.00
print(type(altura))
print(type(peso))

a = 5 + 2j
b = 20 + 6
print(type(a))
print(type(b))

final_de_semana = True
Feriado = False

print(type(final_de_semana))
print(type(Feriado))

alunos = ["Alan", "Rafael", "Carlos", "Talita"]
notas = [10, 9, 8, 7, 6]
print(type(alunos))
print(type(notas))

Valores = (90, 100, 54, 154)
Pontos = (85.5, 90, 100)
print(type(Valores))
print(type(Pontos))

altura2 = {'Alan':1.70, 'Lucas':1.73}
peso2 = {'x':62, 'y':65}
print(type(altura2))
print(type(peso2))